-- ============================================================
-- 1. dim_date
-- Grain : 1 ligne par date calendrier
-- ============================================================

CREATE TABLE IF NOT EXISTS iris_dw.dim_date (
    date_sk INTEGER PRIMARY KEY,          -- format YYYYMMDD, 0 = unknown
    date_val DATE UNIQUE,

    annee INTEGER,
    trimestre INTEGER,
    mois INTEGER,
    nom_mois TEXT,
    jour INTEGER,
    jour_semaine INTEGER,
    nom_jour_semaine TEXT,
    annee_mois TEXT,

    created_at TIMESTAMP NOT NULL DEFAULT now()
);

COMMENT ON TABLE iris_dw.dim_date IS
'Dimension calendrier utilisée par les faits : sinistre, prime et inspection.';

INSERT INTO iris_dw.dim_date (
    date_sk, date_val, annee, trimestre, mois, nom_mois,
    jour, jour_semaine, nom_jour_semaine, annee_mois
)
VALUES (
    0, NULL, NULL, NULL, NULL, 'UNKNOWN',
    NULL, NULL, 'UNKNOWN', 'UNKNOWN'
)
ON CONFLICT (date_sk) DO NOTHING;


