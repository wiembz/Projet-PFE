    nombre_enfants INTEGER,                    -- NBRENF

    date_debut_client DATE,                    -- DEBCNT

    source_system TEXT NOT NULL DEFAULT 'Clients.xlsx',
    created_at TIMESTAMP NOT NULL DEFAULT now(),
    updated_at TIMESTAMP
);

COMMENT ON TABLE iris_dw.dim_client IS
'Dimension client issue de Clients.xlsx. Grain : CNAT + NUMPERS.';

