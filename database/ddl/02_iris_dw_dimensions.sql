-- ============================================================
-- 9. dim_contrat
-- Source : Production.xlsx / Sinistres.xlsx
-- Grain  : 1 ligne par NUMCNT + NUMAVT + NUMMAJ
-- ============================================================

CREATE TABLE IF NOT EXISTS iris_dw.dim_contrat (
    contrat_sk BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    contrat_natural_key TEXT NOT NULL UNIQUE, -- NUMCNT|NUMAVT|NUMMAJ

    num_contrat TEXT NOT NULL,
    num_avenant TEXT,
    num_mise_a_jour TEXT,

    produit_sk BIGINT NOT NULL DEFAULT 0,
    intermediaire_sk BIGINT NOT NULL DEFAULT 0,
    delegation_sk BIGINT NOT NULL DEFAULT 0,

    date_debut_contrat DATE,                  -- DEBCNT
    date_fin_contrat DATE,                    -- FINCNT
    date_debut_effet DATE,                    -- DEBEFFET
    date_fin_effet DATE,                      -- FINEFFET

    duree_contrat TEXT,
    situation_contrat TEXT,                   -- SITUAT
    type_resiliation TEXT,                    -- TYPERESIL
    libelle_resiliation TEXT,                 -- LIB_RESIL

    coassurance TEXT,

    source_system TEXT NOT NULL DEFAULT 'Production.xlsx/Sinistres.xlsx',
    created_at TIMESTAMP NOT NULL DEFAULT now(),
    updated_at TIMESTAMP,

    CONSTRAINT fk_dim_contrat_produit
        FOREIGN KEY (produit_sk)
        REFERENCES iris_dw.dim_produit(produit_sk),

    CONSTRAINT fk_dim_contrat_intermediaire
        FOREIGN KEY (intermediaire_sk)
        REFERENCES iris_dw.dim_intermediaire(intermediaire_sk),

    CONSTRAINT fk_dim_contrat_delegation
        FOREIGN KEY (delegation_sk)
        REFERENCES iris_dw.dim_delegation(delegation_sk)
);

COMMENT ON TABLE iris_dw.dim_contrat IS
'Dimension contrat. Grain : NUMCNT + NUMAVT + NUMMAJ.';

INSERT INTO iris_dw.dim_contrat (
    contrat_sk,
    contrat_natural_key,
    num_contrat,
    num_avenant,
    num_mise_a_jour,
    produit_sk,
    intermediaire_sk,
    delegation_sk
)
VALUES (
    0,
    'UNKNOWN',
    'UNKNOWN',
    'UNKNOWN',
    'UNKNOWN',
    0,
    0,
    0
)
ON CONFLICT (contrat_natural_key) DO NOTHING;


